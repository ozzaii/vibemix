# Test 8 — extrapolation guard (Pitfall 2 defense)
# ---------------------------------------------------------------------------


def test_extrapolation_guard_on_title_change(
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    """On title CHANGE, `_current_position` MUST return the fresh raw elapsed.

    Pitfall 2: nowplaying-cli reports the last sample djay published; when the
    previous poll had the same title with the same elapsed, we extrapolate by
    wall-delta × rate. But when the TITLE changes (e.g. djay advanced to a new
    track), we must NOT extrapolate — return the fresh elapsed value verbatim.
    """
    provider = LookaheadProvider()

    # Seed with a "previous" poll for title "A" at elapsed 12.0 one second ago.
    provider._last_raw = {
        "kMRMediaRemoteNowPlayingInfoTitle": "A",
        "kMRMediaRemoteNowPlayingInfoElapsedTime": 12.0,
        "kMRMediaRemoteNowPlayingInfoPlaybackRate": 1.0,
    }
    provider._last_raw_wall = time.time() - 1.0

    # Now poll returns a DIFFERENT title at fresh elapsed 30.0.
    monkeypatch.setattr(
        provider,
        "_poll_raw",
        lambda: {
            "kMRMediaRemoteNowPlayingInfoTitle": "B",
            "kMRMediaRemoteNowPlayingInfoElapsedTime": 30.0,
            "kMRMediaRemoteNowPlayingInfoPlaybackRate": 1.0,
        },
    )

    title, pos, rate = provider._current_position()
    assert title == "B"
    assert rate == 1.0
    # Critical: pos is the FRESH 30.0 — NOT extrapolated (which would be ≈31.0).
    assert pos == 30.0, (
        f"title changed → must NOT extrapolate; expected 30.0, got {pos}"
    )
