# ---------------------------------------------------------------------------
# Fixture mp3 — generated on-demand via ffmpeg sine. Skips on CI without ffmpeg.
# ---------------------------------------------------------------------------


def _fixture_path() -> Path:
    return Path(__file__).parent / "fixtures" / "test_lookahead_track.mp3"


@pytest.fixture(scope="module")
def lookahead_fixture_mp3() -> Path:
    """A 5s 440Hz sine mp3 generated by ffmpeg; skips if ffmpeg unavailable.

    Module-scoped — generated once per test session for the happy-path test.
    """
    path = _fixture_path()
    if path.exists() and path.stat().st_size > 1024:
        return path
    ffmpeg = shutil.which("ffmpeg")
    if not ffmpeg:
        pytest.skip("ffmpeg not available — happy-path test requires fixture gen")
    path.parent.mkdir(parents=True, exist_ok=True)
    proc = subprocess.run(
        [
            ffmpeg, "-y", "-loglevel", "error",
            "-f", "lavfi", "-i", "sine=frequency=440:duration=5",
            "-c:a", "libmp3lame", "-b:a", "64k", str(path),
        ],
        capture_output=True, timeout=10.0,
    )
    if proc.returncode != 0 or not path.exists():
        pytest.skip(
            f"ffmpeg fixture-gen failed (rc={proc.returncode}): "
            f"{proc.stderr.decode(errors='replace')[:200]}"
        )
    return path


# ---------------------------------------------------------------------------
# Test 1 — happy path
# ---------------------------------------------------------------------------


def test_snapshot_wav_happy_path(
    monkeypatch: pytest.MonkeyPatch, lookahead_fixture_mp3: Path
) -> None:
    """Fixture mp3 + monkeypatched position → valid 16kHz mono WAV bytes."""
    provider = LookaheadProvider()

    monkeypatch.setattr(
        provider, "_current_position", lambda: ("FixtureTrack", 1.0, 1.0)
    )
    monkeypatch.setattr(
        provider, "_resolve_file", lambda title: str(lookahead_fixture_mp3)
    )

    wav, meta = provider.snapshot_wav()
    assert wav is not None, f"happy path returned None; meta={meta}"
    assert wav.startswith(b"RIFF"), "expected WAV RIFF header"
    assert meta["ok"] is True
    assert meta["reason"] == "ok"
    assert meta["title"] == "FixtureTrack"

    with wave.open(io.BytesIO(wav), "rb") as wf:
        assert wf.getframerate() == LOOKAHEAD_SAMPLE_RATE == 16000
        assert wf.getnchannels() == 1
        assert wf.getsampwidth() == 2  # int16
        assert wf.getnframes() > 0


