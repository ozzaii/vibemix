def test_subprocess_args_use_list_form(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    """V10 — ffmpeg invocation MUST use list-form argv (no shell=True)."""
    provider = LookaheadProvider()
    fake_file = tmp_path / "anything.mp3"
    fake_file.write_bytes(b"fake")

    monkeypatch.setattr(
        provider, "_current_position", lambda: ("AnyTrack", 1.0, 1.0)
    )
    monkeypatch.setattr(provider, "_resolve_file", lambda title: str(fake_file))

    fake_proc = MagicMock()
    fake_proc.returncode = 0
    fake_proc.stdout = b"RIFF" + b"\x00" * 1024
    fake_proc.stderr = b""

    with patch(
        "vibemix.audio.lookahead.subprocess.run", return_value=fake_proc
    ) as mock_run:
        provider.snapshot_wav()

    assert mock_run.called
    call_args, call_kwargs = mock_run.call_args
    argv = call_args[0]
    assert isinstance(argv, list), f"argv must be list, got {type(argv).__name__}"
    assert "shell" not in call_kwargs or call_kwargs["shell"] is False, (
        "shell=True is forbidden — V10 Malicious Code mitigation"
    )


def test_input_seek_before_dash_i(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    """Pattern 3 invariant: `-ss` MUST appear BEFORE `-i` (input-seek; ~100× faster)."""
    provider = LookaheadProvider()
    fake_file = tmp_path / "track.mp3"
    fake_file.write_bytes(b"fake")

    monkeypatch.setattr(
        provider, "_current_position", lambda: ("AnyTrack", 30.0, 1.0)
    )
    monkeypatch.setattr(provider, "_resolve_file", lambda title: str(fake_file))

    fake_proc = MagicMock()
    fake_proc.returncode = 0
    fake_proc.stdout = b"RIFF" + b"\x00" * 1024
    fake_proc.stderr = b""

    with patch(
        "vibemix.audio.lookahead.subprocess.run", return_value=fake_proc
    ) as mock_run:
        provider.snapshot_wav()

    argv = mock_run.call_args[0][0]
    assert "-ss" in argv, "ffmpeg argv missing -ss"
    assert "-i" in argv, "ffmpeg argv missing -i"
    assert argv.index("-ss") < argv.index("-i"), (
        "RESEARCH Pattern 3 invariant: `-ss` must precede `-i` (input seek). "
        f"Got argv={argv}"
    )


