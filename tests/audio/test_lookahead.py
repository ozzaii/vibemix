"""Tests for vibemix.audio.lookahead.LookaheadProvider.

Verbatim-behavior port of cohost_v4_tr.py:624-770 — 7 tests cover:
    1. Happy path — file resolves, ffmpeg returns valid 16kHz mono WAV.
    2. No nowplaying — `_poll_raw` returns None → (None, meta).
    3. No mdfind match (streaming track) — `_resolve_file` returns None → (None, meta).
    4. ffmpeg non-zero returncode (e.g. moov-atom-not-found) → (None, meta).
    5. ffmpeg subprocess.TimeoutExpired → (None, meta).
    6. subprocess.run is called with LIST argv (no shell=True) — V10 mitigation.
    7. ffmpeg argv has `-ss` BEFORE `-i` — RESEARCH Pattern 3 invariant.

Plus an extrapolation-guard correctness test (Pitfall 2 defense) for
`_current_position` on title change.

Tests stay HERMETIC: no real nowplaying-cli / mdfind / ffmpeg subprocess
call ever fires from a test body (the optional fixture-generation step
in conftest is the only ffmpeg subprocess we tolerate, and it skips
cleanly on Linux runners that lack ffmpeg).
"""

