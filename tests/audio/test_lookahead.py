def test_ffmpeg_error_returns_none(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    """ffmpeg non-zero returncode (e.g. moov atom not found) ⇒ (None, meta)."""
    provider = LookaheadProvider()
    fake_file = tmp_path / "corrupt.m4a"
    fake_file.write_bytes(b"\x00\x00\x00\x00not actually an mp4")

    monkeypatch.setattr(
        provider, "_current_position", lambda: ("CorruptTrack", 1.0, 1.0)
    )
    monkeypatch.setattr(provider, "_resolve_file", lambda title: str(fake_file))

    fake_proc = MagicMock()
    fake_proc.returncode = 1
    fake_proc.stdout = b""
    fake_proc.stderr = b"moov atom not found"

    with patch(
        "vibemix.audio.lookahead.subprocess.run", return_value=fake_proc
    ) as mock_run:
        wav, meta = provider.snapshot_wav()

    assert wav is None
    assert meta["ok"] is False
    assert "ffmpeg" in meta["reason"]
    assert "moov" in meta["reason"]
    assert mock_run.called, "expected subprocess.run to have been invoked"


def test_subprocess_timeout_returns_none(
    monkeypatch: pytest.MonkeyPatch, tmp_path: Path
) -> None:
    """ffmpeg TimeoutExpired ⇒ (None, meta) — caller never sees an exception."""
    provider = LookaheadProvider()
    fake_file = tmp_path / "slow.flac"
    fake_file.write_bytes(b"fake-flac-data")

    monkeypatch.setattr(
        provider, "_current_position", lambda: ("SlowTrack", 1.0, 1.0)
    )
    monkeypatch.setattr(provider, "_resolve_file", lambda title: str(fake_file))

    def _raise_timeout(*args, **kwargs):  # noqa: ANN001 — mock signature
        raise subprocess.TimeoutExpired(cmd="ffmpeg", timeout=4.0)

    with patch(
        "vibemix.audio.lookahead.subprocess.run", side_effect=_raise_timeout
    ):
        wav, meta = provider.snapshot_wav()

    assert wav is None
    assert meta["ok"] is False
    assert "timeout" in meta["reason"].lower()


